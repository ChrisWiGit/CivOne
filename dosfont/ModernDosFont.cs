        public static byte[] GetGlyphRows(byte cp437Code)
        {
            var rows = new byte[GlyphHeight];

            for (int y = 0; y < GlyphHeight; y++)
                rows[y] = FontData[cp437Code, y];

            return rows;
        }

        public static byte GetRow(byte cp437Code, int y)
        {
            if ((uint)y >= GlyphHeight)
                throw new ArgumentOutOfRangeException(nameof(y));

            return FontData[cp437Code, y];
        }

        public static bool IsPixelSet(byte cp437Code, int x, int y)
        {
            if ((uint)x >= GlyphWidth)
                throw new ArgumentOutOfRangeException(nameof(x));

            if ((uint)y >= GlyphHeight)
                throw new ArgumentOutOfRangeException(nameof(y));

            byte row = FontData[cp437Code, y];

            // x = 0 reads bit 7, the leftmost pixel.
            return (row & (1 << (7 - x))) != 0;
        }

        public static bool TryGetCp437(char ch, out byte cp437Code)
        {
            return Cp437ByUnicode.TryGetValue(ch, out cp437Code);
        }

        private static IReadOnlyDictionary<char, byte> CreateCp437Lookup()
        {
            var map = new Dictionary<char, byte>(GlyphCount);

            for (int i = 0; i < UnicodeByCp437.Length; i++)
            {
                char ch = UnicodeByCp437[i];

                // Keep the first CP437 code if a Unicode character appears more than once.
                if (!map.ContainsKey(ch))
                    map.Add(ch, (byte)i);
            }

            return map;
        }
